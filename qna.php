<div class="accordion" id="accordionExample"style="margin-top: 40px">
  <div class="card">
    <div class="card-header" id="headingOne">
      <h2 class="mb-0">
        <button class="btn btn-link text-dark" type="button" data-toggle="collapse" data-target="#collapseOne" aria-expanded="true" aria-controls="collapseOne">
        <h5>Bagaimana cara untuk menawarkan kerja sama kepada Pemerintah Kota Yogyakarta?</5>
        </button>
      </h2>
    </div>

    <div id="collapseOne" class="collapse show" aria-labelledby="headingOne" data-parent="#accordionExample">
      <div class="card-body">
      Pemerintah Kota Yogyakarta dapat bekerjasama dengan organisasi swasta maupun pemerintahan (kementerian/lembaga/badan/pemda lainnya) baik dalam maupun luar negeri. Untuk melaksanakan kerja sama tersebut, seluruh pemerintah daerah di Indonesia harus mengikuti tata cara pelaksanaan kerja sama daerah (KSD) yang diatur dalam Peraturan Menteri Dalam Negeri Republik Indonesia Nomor 22 Tahun 2020 (untuk kerja sama dengan entitas dalam negeri) dan Peraturan Menteri Dalam Negeri Republik Indonesia Nomor 25 Tahun 2020 (untuk kerja sama dengan entitas luar negeri). Untuk mengetahui prosedur dan persyaratan untuk menawarkan kerja sama daerah kepada Pemerintah Kota Yogyakarta, silahkan klik di sini.
      </div>
    </div>
  </div>
  <div class="card">
    <div class="card-header" id="headingTwo">
      <h2 class="mb-0">
        <button class="btn btn-link  text-dark" type="button" data-toggle="collapse" data-target="#collapseTwo" aria-expanded="false" aria-controls="collapseTwo">
        <h5>Ada tawaran kerja sama daerah dari pihak lain, apa yang harus dilakukan oleh OPD?</h5>
        </button>
      </h2>
    </div>
    <div id="collapseTwo" class="collapse" aria-labelledby="headingTwo" data-parent="#accordionExample">
      <div class="card-body">
      <b>Langkah pertama</b>, diskusikan dengan pihak yang menawarkan kerja sama daerah (KSD) secara mendalam tentang: objek yang akan dikerjasamakan, ruang lingkup, manfaat dan pembiayaan. Pastikan pula pembagian hak dan kewajiban di antara para pihak sesuai dengan kewenangan masing-masing. <b>Langkah kedua</b>, sampaikan kepada pihak yang menawarkan KSD untuk mengikuti prosedur yang tertera pada menu <b>UNTUK MITRA</b> (klik di sini). Jika diperlukan, silahkan untuk menghubungi Sub Bagian Kerjasama, Bagian P3ADK melalui nomor ekstensi 578.
      </div>
    </div>
  </div>
  <div class="card">
    <div class="card-header" id="headingThree">
      <h2 class="mb-0">
        <button class="btn btn-link  text-dark" type="button" data-toggle="collapse" data-target="#collapseThree" aria-expanded="false" aria-controls="collapseThree">
        <h5>Apakah Kepala OPD dapat langsung melaksanakan penawaran kerja sama daerah kepada pihak lain?</h5>
        </button>
      </h2>
    </div>
    <div id="collapseThree" class="collapse" aria-labelledby="headingThree" data-parent="#accordionExample">
      <div class="card-body">
      Dalam Peraturan Menteri Dalam Negeri Republik Indonesia Nomor 22 Tahun 2020 (<a href="https://drive.google.com/file/d/19ULMldzI5y9rQNOnWbe0hdqDdN0DCE_R/view"><u><b>klik di sini</b></u></a>), <b>penawaran kerja sama daerah</b> dengan daerah lain (KSDD) atau dengan pihak ketiga (KSDPK) atau dengan kementerian/lembaga/badan (pemerintah pusat), <b>dilaksanakan oleh kepala daerah</b>. OPD melakukan perencanaan dan inisiasi KSD yang akan dikaji terlebih dahulu oleh Tim Koordinasi Kerja Sama Daerah (TKKSD) Kota Yogyakarta. Apabila rencana KSD dinilai layak, TKKSD akan menyusun surat penawaran KSD yang akan ditandatangani oleh Walikota Yogyakarta.
      OPD yang akan melaksanakan penawaran kerja sama daerah, harap mempelajari <i>business process</i> dan mekanisme kerja sama daerah (<a href="https://docs.google.com/presentation/d/1ft1MZsPfaAbEiCtx2DSXOpHUldEyaXfvCburqTUpV0E/edit"><u><b>klik di sini</b></u></a>). Pelaksanaan kerja sama daerah di Pemerintah Kota Yogyakarta dikoordinasikan oleh Sekretaris Daerah selaku Ketua TKKSD.
      </div>
    </div>
  </div>
  <div class="card">
    <div class="card-header" id="heading4">
      <h2 class="mb-0">
        <button class="btn btn-link  text-dark" type="button" data-toggle="collapse" data-target="#collapse4" aria-expanded="false" aria-controls="collapseThree">
        <h5>Siapakah yang melakukan perancangan dokumen kerja sama (MoU/PKS/lainnya?)</h5>
        </button>
      </h2>
    </div>
    <div id="collapse4" class="collapse" aria-labelledby="heading4" data-parent="#accordionExample">
      <div class="card-body">
      Terdapat beberapa jenis dokumen kerja sama daerah (KSD) yang diakui dalam <b><u>Peraturan Menteri Dalam Negeri Republik Indonesia Nomor 22 Tahun 2020.</u></b> Pihak yang menyusun naskah/dokumen KSD dibedakan menurut jenis dokumennya. Silahkan mempelajari materi KSD untuk mengetahui dokumen apa saja yang dihasilkan dan siapa yang bertanggungjawab untuk menyusunnya (<a href="https://docs.google.com/presentation/d/1ft1MZsPfaAbEiCtx2DSXOpHUldEyaXfvCburqTUpV0E/edit"><u><b>klik di sini</b></u></a>).
      </div>
    </div>
  </div>
  
  <div class="card">
    <div class="card-header" id="heading5">
      <h2 class="mb-0">
        <button class="btn btn-link text-dark" type="button" data-toggle="collapse" data-target="#collapse5" aria-expanded="false" aria-controls="collapseThree">
        <h5>Apakah OPD dapat melaksanakan perjanjian kerja sama dengan instansi pemerintah pusat (kementerian/lembaga/badan)?</h5>
        </button>
      </h2>
    </div>
    <div id="collapse5" class="collapse" aria-labelledby="heading5" data-parent="#accordionExample">
      <div class="card-body">
      <b>Ya, dapat.</b> Pelaksanaan kerja sama antara Pemerintah Kota Yogyakarta dengan instansi pemerintah pusat dilaksanakan dengan menyusun Nota Kesepakatan (<a href="https://drive.google.com/file/d/1grLYr3RdEQJeRQfZtmV5kEmHHMhjqYQA/view"><u><b>klik di sini</b></u></a>) yang harus dilampiri dengan rencana kerja (<a href="https://drive.google.com/file/d/1In2venevDV6ODCSRe1g_0uqpxZerYo_o/view"><u><b>klik di sini</b></u></a>). <b>Tidak ada lagi perjanjian kerja sama</b> untuk melaksanakan kerja sama daerah dengan instansi pemerintah pusat. Dasar hukum yang digunakan paling tidak ada 3 (tiga), yaitu: <b>UU No.23/2014 tentang Pemerintahan Daerah</b> (<a href="https://drive.google.com/file/d/16fdIOCQbuezf52uuP8_IQnfncoTQ-_eJ/view"><u><b>klik di sini</b></u></a>), <b>PP No.28/2018 tentang Kerja Sama Daerah</b> (<a href="https://drive.google.com/file/d/1h5apdj4w5gGENKs5o3IzMxIG7gtX0q4N/view"><u><b>klik di sini</b></u></a>) dan <b>Permendagri No.22/2020 tentang Tata Cara Kerja Sama Daerah dengan Daerah Lain dan Kerja Sama Daerah dengan Pihak Ketiga</b>(<a href="https://drive.google.com/file/d/19ULMldzI5y9rQNOnWbe0hdqDdN0DCE_R/view"><u><b>klik di sini</b></u></a>).
      </div>
    </div>
  </div>
   
  <div class="card">
    <div class="card-header" id="heading6">
      <h2 class="mb-0">
        <button class="btn btn-link text-dark" type="button" data-toggle="collapse" data-target="#collapse6" aria-expanded="false" aria-controls="collapseThree">
        <h5>Siapakah yang dapat menandatangani dokumen perjanjian kerja sama?</h5>
        </button>
      </h2>
    </div>
    <div id="collapse6" class="collapse" aria-labelledby="heading6" data-parent="#accordionExample">
      <div class="card-body">
      Walikota Yogyakarta pada prinsipnya dapat menandatangani seluruh naskah perjanjian kerja sama daerah, meliputi: Kesepakatan Bersama (KB), Nota Kesepakatan (NK), Perjanjian Kerja Sama (PKS), <i>Letter of Intent</i> (LoI, dengan lembaga asing) dan <i>Memorandum of Understanding</i> (MoU, dengan lembaga asing). Namun demikian, pejabat yang diberikan kuasa oleh Walikota Yogyakarta hanya dapat menandatangani PKS.
      </div>
    </div>
  </div>
  <div class="card">
    <div class="card-header" id="heading7">
      <h2 class="mb-0">
        <button class="btn btn-link text-dark" type="button" data-toggle="collapse" data-target="#collapse7" aria-expanded="false" aria-controls="collapseThree">
        <h5>Berapa tahun jangka waktu maksimal suatu perjanjian kerja sama? dan apakah dapat diperpanjang?</h5>
        </button>
      </h2>
    </div>
    <div id="collapse7" class="collapse" aria-labelledby="heading7" data-parent="#accordionExample">
      <div class="card-body">
      Dokumen perjanjian kerja sama daerah dapat berlaku paling lama 5 (lima) tahun dan dapat diperpanjang atas kesepakatan para pihak, kecuali ditentukan lain oleh peraturan perundang-undangan yang mengatur khusus mengenai objek atau tata cara kerja sama tertentu.
      </div>
    </div>
  </div>
  <div class="card">
    <div class="card-header" id="heading8">
      <h2 class="mb-0">
        <button class="btn btn-link text-dark" type="button" data-toggle="collapse" data-target="#collapse8" aria-expanded="false" aria-controls="collapseThree">
        <h5>Apakah hibah daerah termasuk dalam perjanjian kerja sama?</h5>
        </button>
      </h2>
    </div>
    <div id="collapse8" class="collapse" aria-labelledby="heading8" data-parent="#accordionExample">
      <div class="card-body">
      <b>Hibah daerah</b> dapat merupakan bagian dari pelaksanaan suatu kerja sama daerah (KSD) dan dapat pula merupakan suatu kegiatan tersendiri yang terlepas dari mekanisme KSD. Penatalaksanaan hibah daerah baik sebagai kegiatan tersendiri maupun sebagai bagian dari pelaksanaan KSD mengikuti <b>Peraturan Menteri Dalam Negeri Republik Indonesia Nomor 32 Tahun 2011</b> (<a href="https://drive.google.com/file/d/1D5091r3IUlZb72x6VJ--7zgrVDLpeGe6/view"><u><b>klik di sini</b></u></a>) dan peraturan perubahannya yaitu <b>Peraturan Menteri Dalam Negeri Republik Indonesia Nomor 13 Tahun 2018</b> (<a href="https://drive.google.com/file/d/19qrP15QAlFQ-KPQPmKHw4vVUyuwLROOz/view"><u><b>klik di sini</b></u></a>). Untuk pelaksanaan hibah daerah, dapat dikoordinasikan dengan Bidang Anggaran pada Badan Pengelola Keuangan dan Aset Daerah (BPKAD) Kota Yogyakarta.
      </div>
    </div>
  </div>
  <div class="card">
    <div class="card-header" id="heading9">
      <h2 class="mb-0">
        <button class="btn btn-link text-dark" type="button" data-toggle="collapse" data-target="#collapse9" aria-expanded="false" aria-controls="collapseThree">
        <h5>Apakah itu Tim Koordinasi Kerja Sama Daerah (TKKSD)?</h5>
        </button>
      </h2>
    </div>
    <div id="collapse9" class="collapse" aria-labelledby="heading9" data-parent="#accordionExample">
      <div class="card-body">
      TKKSD adalah tim yang dibentuk oleh kepala daerah (Walikota Yogyakarta) untuk membantu kepala daerah dalam menyiapkan kerja sama daerah. TKKSD Kota Yogyakarta ditetapkan dengan Keputusan Walikota Yogyakarta Nomor 55 Tahun 2020 (<a href="https://drive.google.com/file/d/18LqYdGX4S5K-n_zgQnCWvJQpX4fwrDgU/view"><u><b>klik di sini</b></u></a>).
      </div>
    </div>
  </div>
  <div class="card">
    <div class="card-header" id="heading10">
      <h2 class="mb-0">
        <button class="btn btn-link text-dark" type="button" data-toggle="collapse" data-target="#collapse10" aria-expanded="false" aria-controls="collapseThree">
        <h5>Apakah perpanjangan perjanjian kerja sama (PKS) tetap harus mengikuti prosedur kerja sama daerah (KSD) yang baru?</h5>
        </button>
      </h2>
    </div>
    <div id="collapse10" class="collapse" aria-labelledby="heading10" data-parent="#accordionExample">
      <div class="card-body">
      Ya. Perpanjangan perjanjian kerja sama (PKS) harus mengikuti prosedur kerja sama daerah (KSD) terbaru yang terdapat dalam website ini. Silahkan mengunjungi menu <b>UNTUK OPD</b> ( klik di sini).
      </div>
    </div>
  </div>
