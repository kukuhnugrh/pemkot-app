    <div class="card-header" id="heading11">
      <h2 class="mb-0">
        <button class="btn btn-link text-dark" type="button" data-toggle="collapse" data-target="#collapse11" aria-expanded="false" aria-controls="collapseThree">
        <h5>Apakah pemerintah daerah dapat langsung melaksanakan kerja sama dengan lembaga asing?</h5>
        </button>
      </h2>
    </div>
    <div id="collapse11" class="collapse" aria-labelledby="heading11" data-parent="#accordionExample">
      <div class="card-body">
      <b>Tidak.</b> Setiap perjanjian kerja sama yang dilakukan dengan pemerintah daerah di luar negeri, atau lembaga internasional atau badan swasta asing harus mendapat persetujuan pemerintah pusat terlebih dahulu setelah disetujui oleh DPRD Kota Yogyakarta.Silahkan merujuk pada <b>Peraturan Menteri Dalam Negeri Republik Indonesia Nomor 25 Tahun 2020</b>(<a href="https://drive.google.com/file/d/1CK0Rxq784nozqV06ICIkXBANwb8skt5T/view"><u><b>klik di sini</b></u></a>)
